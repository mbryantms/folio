"use client";

import { useMemo, useState } from "react";

import { Button } from "@/components/ui/button";
import { Card, CardContent } from "@/components/ui/card";
import { Skeleton } from "@/components/ui/skeleton";
import { Switch } from "@/components/ui/switch";
import { Label } from "@/components/ui/label";
import {
  AlertDialog,
  AlertDialogAction,
  AlertDialogCancel,
  AlertDialogContent,
  AlertDialogDescription,
  AlertDialogFooter,
  AlertDialogHeader,
  AlertDialogTitle,
} from "@/components/ui/alert-dialog";
import { useThumbnailsSettings, useThumbnailsStatus } from "@/lib/api/queries";
import {
  useClearQueue,
  useDeleteAllThumbnails,
  useForceRecreateThumbnails,
  useGenerateMissingThumbnails,
  useGeneratePageMapThumbnails,
  useUpdateThumbnailsSettings,
} from "@/lib/api/mutations";
import { useScanEvents } from "@/lib/api/scan-events";
import { cn } from "@/lib/utils";
import type { ScanEvent, ThumbnailFormat } from "@/lib/api/types";

const FORMATS: { value: ThumbnailFormat; label: string; hint: string }[] = [
  { value: "webp", label: "WebP", hint: "Smallest files, modern browsers" },
  { value: "jpeg", label: "JPEG", hint: "Universal compatibility, lossy" },
  { value: "png", label: "PNG", hint: "Lossless, larger files" },
];

/**
 * Library Thumbnails admin tab. Three sections:
 *   1. Settings — enable/disable + format selector.
 *   2. Actions — Generate missing / Force recreate / Delete all.
 *   3. Progress + queue — live progress bar and an event log fed by the
 *      WebSocket scan-event stream.
 */
export function ThumbnailsAdmin({ libraryId }: { libraryId: string }) {
  const settings = useThumbnailsSettings(libraryId);
  const status = useThumbnailsStatus(libraryId);
  const updateSettings = useUpdateThumbnailsSettings(libraryId);
  const generateMissing = useGenerateMissingThumbnails(libraryId);
  const generatePageMap = useGeneratePageMapThumbnails(libraryId);
  const forceRecreate = useForceRecreateThumbnails(libraryId);
  const deleteAll = useDeleteAllThumbnails(libraryId);
  const clearQueue = useClearQueue();
  const { events: liveEvents, status: wsStatus } = useScanEvents({ libraryId });

  const [confirmRecreate, setConfirmRecreate] = useState(false);
  const [confirmDelete, setConfirmDelete] = useState(false);
  const [confirmClearQueue, setConfirmClearQueue] = useState(false);

  const enabled = settings.data?.enabled ?? true;
  const format = settings.data?.format ?? "webp";
  const anyMutating =
    updateSettings.isPending ||
    generateMissing.isPending ||
    generatePageMap.isPending ||
    forceRecreate.isPending ||
    deleteAll.isPending ||
    clearQueue.isPending;

  // Recent thumbnail events for the live log. Filter to thumbs.* and
  // newest-first so the log reads top-down.
  const thumbEvents = useMemo(
    () =>
      liveEvents
        .filter((e) => e.type.startsWith("thumbs."))
        .slice(-25)
        .reverse(),
    [liveEvents],
  );

  if (settings.isLoading || status.isLoading) {
    return (
      <div className="space-y-6">
        <Skeleton className="h-32 w-full" />
        <Skeleton className="h-32 w-full" />
        <Skeleton className="h-48 w-full" />
      </div>
    );
  }
  if (settings.error || !settings.data || status.error || !status.data) {
    return (
      <Card>
        <CardContent className="text-destructive p-4 text-sm">
          Failed to load thumbnail status.
        </CardContent>
      </Card>
    );
  }

  const d = status.data;
  const percent =
    d.total === 0 ? 100 : Math.round((d.cover_generated / d.total) * 100);
  const pagePercent =
    d.page_total === 0
      ? 100
      : Math.round((d.page_map_generated / d.page_total) * 100);
  const queueActive = d.in_flight > 0;

  return (
    <div className="space-y-6">
      {/* ── Settings ─────────────────────────────────────────── */}
      <Card>
        <CardContent className="space-y-5 p-5">
          <div>
            <h3 className="text-foreground text-sm font-semibold tracking-tight">
              Thumbnail settings
            </h3>
            <p className="text-muted-foreground mt-1 text-xs">
              Pre-generated by the post-scan worker. Changes apply to new
              generation only — switch the format and run &quot;Force
              recreate&quot; to apply it everywhere.
            </p>
          </div>

          <div className="flex items-start justify-between gap-4">
            <div className="space-y-0.5">
              <Label htmlFor="thumbs-enabled" className="text-sm font-medium">
                Generate thumbnails
              </Label>
              <p className="text-muted-foreground text-xs">
                When off, scans skip thumbnail generation. Existing thumbnails
                keep serving from disk.
              </p>
            </div>
            <Switch
              id="thumbs-enabled"
              checked={enabled}
              disabled={anyMutating}
              onCheckedChange={(checked) => {
                updateSettings.mutate({ enabled: checked });
              }}
            />
          </div>

          <div className="space-y-2">
            <Label className="text-sm font-medium">Image format</Label>
            <div
              role="radiogroup"
              aria-label="Thumbnail image format"
              className="grid gap-2 sm:grid-cols-3"
            >
              {FORMATS.map((opt) => {
                const active = format === opt.value;
                return (
                  <button
                    key={opt.value}
                    type="button"
                    role="radio"
                    aria-checked={active}
                    disabled={anyMutating || !enabled}
                    onClick={() => {
                      if (format !== opt.value) {
                        updateSettings.mutate({ format: opt.value });
                      }
                    }}
                    className={cn(
                      "rounded-md border px-3 py-2 text-left text-sm transition-colors",
                      active
                        ? "border-primary bg-primary/5 text-foreground"
                        : "border-border bg-background text-muted-foreground hover:text-foreground",
                      (anyMutating || !enabled) && "opacity-60",
                    )}
                  >
                    <div className="font-medium tracking-wide uppercase">
                      {opt.label}
                    </div>
                    <div className="text-muted-foreground text-xs">
                      {opt.hint}
                    </div>
                  </button>
                );
              })}
            </div>
            {!enabled ? (
              <p className="text-muted-foreground text-xs">
                Format takes effect when generation is re-enabled.
              </p>
            ) : null}
          </div>
        </CardContent>
      </Card>

      {/* ── Actions ──────────────────────────────────────────── */}
      <Card>
        <CardContent className="space-y-4 p-5">
          <div>
            <h3 className="text-foreground text-sm font-semibold tracking-tight">
              Actions
            </h3>
            <p className="text-muted-foreground mt-1 text-xs">
              <strong>Generate missing covers</strong> warms issue cover
              thumbnails. <strong>Generate missing pages</strong> fills the
              reader strip thumbnails in the background — existing pages are
              skipped. <strong>Force recreate</strong> wipes and rebuilds all
              thumbnails — required after switching format.
            </p>
          </div>

          <div className="flex flex-wrap gap-2">
            <Button
              type="button"
              variant="default"
              size="sm"
              disabled={!enabled || anyMutating || d.total === 0}
              onClick={() => generateMissing.mutate()}
            >
              {generateMissing.isPending
                ? "Enqueueing…"
                : "Generate missing covers"}
            </Button>
            <Button
              type="button"
              variant="outline"
              size="sm"
              disabled={!enabled || anyMutating || d.total === 0}
              onClick={() => generatePageMap.mutate()}
            >
              {generatePageMap.isPending
                ? "Queueing…"
                : "Generate missing pages"}
            </Button>
            <Button
              type="button"
              variant="outline"
              size="sm"
              disabled={!enabled || anyMutating || d.total === 0}
              onClick={() => setConfirmRecreate(true)}
            >
              {forceRecreate.isPending ? "Enqueueing…" : "Force recreate all"}
            </Button>
            <Button
              type="button"
              variant="outline"
              size="sm"
              disabled={anyMutating || d.total === 0}
              onClick={() => setConfirmDelete(true)}
              className="text-destructive hover:text-destructive"
            >
              {deleteAll.isPending ? "Deleting…" : "Delete all thumbnails"}
            </Button>
          </div>
        </CardContent>
      </Card>

      {/* ── Progress + queue ─────────────────────────────────── */}
      <Card>
        <CardContent className="space-y-4 p-5">
          <div className="flex flex-wrap items-baseline justify-between gap-3">
            <div>
              <h3 className="text-foreground text-sm font-semibold tracking-tight">
                Progress
              </h3>
              <p className="text-muted-foreground mt-1 text-xs">
                Cover readiness is tracked separately from background page-map
                jobs.
              </p>
            </div>
            <span className="text-muted-foreground/70 ml-auto text-xs tabular-nums">
              v{d.current_version}
            </span>
          </div>

          <div className="grid gap-4 lg:grid-cols-2">
            <div className="border-border bg-background/50 flex min-h-32 flex-col justify-between rounded-md border p-4">
              <div className="flex items-baseline justify-between gap-3">
                <div>
                  <p className="text-muted-foreground text-xs font-medium tracking-widest uppercase">
                    Cover readiness
                  </p>
                  <p className="text-muted-foreground text-xs">
                    Used by library, series, and issue grids.
                  </p>
                </div>
                <span className="text-muted-foreground text-sm tabular-nums">
                  {percent}%
                </span>
              </div>
              <div className="space-y-3">
                <div className="flex items-baseline justify-between">
                  <span className="text-foreground text-2xl font-semibold tabular-nums">
                    {d.cover_generated}
                    <span className="text-muted-foreground text-base">
                      {" "}
                      / {d.total}
                    </span>
                  </span>
                </div>
                <div
                  className="bg-muted h-2 w-full overflow-hidden rounded-full"
                  role="progressbar"
                  aria-valuenow={percent}
                  aria-valuemin={0}
                  aria-valuemax={100}
                  aria-label="Cover thumbnail readiness"
                >
                  <div
                    className="bg-primary h-full transition-[width] duration-500"
                    style={{ width: `${percent}%` }}
                  />
                </div>
              </div>
            </div>

            <div
              className={cn(
                "flex min-h-32 flex-col justify-between rounded-md border p-4",
                queueActive
                  ? "border-amber-800/40 bg-amber-950/20"
                  : "border-border bg-background/50",
              )}
            >
              <div className="flex items-baseline justify-between gap-3">
                <div>
                  <p className="text-muted-foreground text-xs font-medium tracking-widest uppercase">
                    Page-map readiness
                  </p>
                  <p className="text-muted-foreground text-xs">
                    Used by the reader page strip.
                  </p>
                </div>
                <span
                  className={cn(
                    "rounded-full border px-2 py-0.5 text-xs font-semibold tabular-nums",
                    queueActive
                      ? "border-amber-800/40 bg-amber-950/30 text-amber-300"
                      : "border-border text-muted-foreground",
                  )}
                >
                  {pagePercent}%
                </span>
              </div>
              <div className="space-y-3">
                <div className="flex items-baseline justify-between">
                  <span className="text-foreground text-2xl font-semibold tabular-nums">
                    {d.page_map_generated}
                    <span className="text-muted-foreground text-base">
                      {" "}
                      / {d.page_total}
                    </span>
                  </span>
                </div>
                <div
                  className="bg-muted h-2 w-full overflow-hidden rounded-full"
                  role="progressbar"
                  aria-valuenow={pagePercent}
                  aria-valuemin={0}
                  aria-valuemax={100}
                  aria-label="Page-map thumbnail readiness"
                >
                  <div
                    className="h-full bg-amber-500 transition-[width] duration-500"
                    style={{ width: `${pagePercent}%` }}
                  />
                </div>
                <Button
                  type="button"
                  variant="outline"
                  size="sm"
                  disabled={clearQueue.isPending || d.in_flight === 0}
                  onClick={() => setConfirmClearQueue(true)}
                  className="w-fit text-xs"
                >
                  {clearQueue.isPending
                    ? "Clearing..."
                    : "Clear thumbnail queue"}
                </Button>
              </div>
            </div>
          </div>

          <dl className="grid grid-cols-2 gap-3 text-xs sm:grid-cols-4">
            <Stat label="Missing covers" value={d.cover_missing} />
            <Stat label="Queued covers" value={d.cover_queued} />
            <Stat label="Missing page maps" value={d.page_map_missing} />
            <Stat label="Queued page maps" value={d.page_map_queued} />
            <Stat
              label="Errored jobs"
              value={d.cover_failed}
              tone={d.cover_failed > 0 ? "destructive" : "default"}
            />
            <Stat
              label="Queue depth"
              value={d.in_flight}
              tone={d.in_flight > 0 ? "primary" : "default"}
            />
            <Stat label="Ready covers" value={d.cover_generated} />
            <Stat label="Ready page maps" value={d.page_map_generated} />
          </dl>

          <div className="space-y-1">
            <p className="text-muted-foreground text-xs font-medium tracking-widest uppercase">
              Recent activity
            </p>
            {thumbEvents.length === 0 ? (
              <p className="text-muted-foreground text-xs">
                {d.in_flight > 0
                  ? `Waiting for thumbnail events (${wsStatus})…`
                  : "No recent thumbnail events."}
              </p>
            ) : (
              <ul className="divide-border border-border bg-background/50 divide-y rounded-md border">
                {thumbEvents.map((e, i) => (
                  <li
                    key={`${e.type}-${"issue_id" in e ? e.issue_id : ""}-${i}`}
                    className="flex items-center gap-3 px-3 py-1.5 text-xs"
                  >
                    <EventDot type={e.type} />
                    <span className="text-muted-foreground font-mono">
                      {labelFor(e)}
                    </span>
                    {"issue_id" in e ? (
                      <span className="text-muted-foreground/70 ml-auto truncate font-mono">
                        {e.issue_id.slice(0, 8)}…
                      </span>
                    ) : null}
                  </li>
                ))}
              </ul>
            )}
          </div>
        </CardContent>
      </Card>

      <AlertDialog open={confirmRecreate} onOpenChange={setConfirmRecreate}>
        <AlertDialogContent>
          <AlertDialogHeader>
            <AlertDialogTitle>Recreate all thumbnails?</AlertDialogTitle>
            <AlertDialogDescription>
              Wipes the {d.total} cover and strip thumbnails for this library
              and enqueues fresh jobs in {format.toUpperCase()}. The reader will
              fall back to inline generation in the meantime.
            </AlertDialogDescription>
          </AlertDialogHeader>
          <AlertDialogFooter>
            <AlertDialogCancel disabled={forceRecreate.isPending}>
              Cancel
            </AlertDialogCancel>
            <AlertDialogAction
              onClick={() =>
                forceRecreate.mutate(undefined, {
                  onSettled: () => setConfirmRecreate(false),
                })
              }
              disabled={forceRecreate.isPending}
            >
              {forceRecreate.isPending ? "Enqueueing…" : "Recreate"}
            </AlertDialogAction>
          </AlertDialogFooter>
        </AlertDialogContent>
      </AlertDialog>

      <AlertDialog open={confirmDelete} onOpenChange={setConfirmDelete}>
        <AlertDialogContent>
          <AlertDialogHeader>
            <AlertDialogTitle>Delete all thumbnails?</AlertDialogTitle>
            <AlertDialogDescription>
              Removes every cover and strip thumbnail on disk for this library
              and clears the database state. New thumbnails will not be
              generated until you click &quot;Generate missing covers&quot; (or
              &quot;Generate missing pages&quot; for the reader strip) or run a
              scan
              {!enabled ? " (and re-enable generation)" : ""}.
            </AlertDialogDescription>
          </AlertDialogHeader>
          <AlertDialogFooter>
            <AlertDialogCancel disabled={deleteAll.isPending}>
              Cancel
            </AlertDialogCancel>
            <AlertDialogAction
              onClick={() =>
                deleteAll.mutate(undefined, {
                  onSettled: () => setConfirmDelete(false),
                })
              }
              disabled={deleteAll.isPending}
              className="bg-destructive text-destructive-foreground hover:bg-destructive/90"
            >
              {deleteAll.isPending ? "Deleting…" : "Delete all"}
            </AlertDialogAction>
          </AlertDialogFooter>
        </AlertDialogContent>
      </AlertDialog>

      <AlertDialog open={confirmClearQueue} onOpenChange={setConfirmClearQueue}>
        <AlertDialogContent>
          <AlertDialogHeader>
            <AlertDialogTitle>Clear thumbnail queue?</AlertDialogTitle>
            <AlertDialogDescription>
              Removes pending cover and page-map thumbnail jobs. It does not
              delete existing thumbnail files or change thumbnail settings. A
              thumbnail already being generated may still finish.
            </AlertDialogDescription>
          </AlertDialogHeader>
          <AlertDialogFooter>
            <AlertDialogCancel disabled={clearQueue.isPending}>
              Cancel
            </AlertDialogCancel>
            <AlertDialogAction
              onClick={() =>
                clearQueue.mutate(
                  { target: "thumbnails" },
                  { onSettled: () => setConfirmClearQueue(false) },
                )
              }
              disabled={clearQueue.isPending}
              className="bg-destructive text-destructive-foreground hover:bg-destructive/90"
            >
              {clearQueue.isPending ? "Clearing..." : "Clear queue"}
            </AlertDialogAction>
          </AlertDialogFooter>
        </AlertDialogContent>
      </AlertDialog>
    </div>
  );
}

function Stat({
  label,
  value,
  tone = "default",
}: {
  label: string;
  value: number;
  tone?: "default" | "primary" | "destructive";
}) {
  return (
    <div>
      <dt className="text-muted-foreground text-[10px] font-medium tracking-widest uppercase">
        {label}
      </dt>
      <dd
        className={cn(
          "text-base font-semibold tabular-nums",
          tone === "primary" && "text-primary",
          tone === "destructive" && "text-destructive",
        )}
      >
        {value}
      </dd>
    </div>
  );
}

function EventDot({ type }: { type: ScanEvent["type"] }) {
  const tone =
    type === "thumbs.completed"
      ? "bg-primary"
      : type === "thumbs.failed"
        ? "bg-destructive"
        : "bg-muted-foreground/60";
  return <span className={cn("h-1.5 w-1.5 shrink-0 rounded-full", tone)} />;
}

function labelFor(e: ScanEvent): string {
  const kind =
    "kind" in e && e.kind === "page_map"
      ? "Page map"
      : "kind" in e && e.kind === "cover_page_map"
        ? "Cover and page map"
        : "kind" in e && e.kind === "cover"
          ? "Cover"
          : "Thumbnail";
  switch (e.type) {
    case "thumbs.started":
      return `${kind} started`;
    case "thumbs.completed":
      return e.kind === "page_map"
        ? `Page map completed (${e.pages} page${e.pages === 1 ? "" : "s"})`
        : e.kind === "cover_page_map"
          ? `Cover and page map completed (${e.pages} image${e.pages === 1 ? "" : "s"})`
          : `${kind} completed`;
    case "thumbs.failed":
      return `${kind} failed: ${e.error}`;
    default:
      return e.type;
  }
}
